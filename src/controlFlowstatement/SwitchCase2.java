package controlFlowstatement;

public class SwitchCase2 {

	public static void main(String[] args) {
		int key = 3;
      switch (key) {
	case 1: {
		System.out.println("This is a key1");
	}
	case 2:{
		System.out.println("This is a key2");
	}
	case 3:{		
		System.out.println("This is a key 3");
	}
    default:
		System.out.println("This is a default key");
	}
	}

}
